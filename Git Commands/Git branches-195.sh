## 1. Git branches ##

~/chatbot$ git checkout more-speech

## 2. Switching branches ##

~/chatbot$ git commit -m "Added more ouput"

## 3. Pushing a branch to a remote ##

~/chatbot$ git push origin more-speech

## 4. Merging branches ##

~/chatbot$ git push origin master

## 5. Deleting branches ##

~/chatbot$ git branch -d more-speech

## 6. Checking out branches from the remote ##

~/chatbot$ python bot.py

## 7. Finding differences across branches ##

~/chatbot$ git diff master happy-bot

## 8. Branch naming conventions ##

~/chatbot$ git push origin feature/random-messages

## 9. Branch history ##

~/chatbot$ git checkout feature/spam-messages